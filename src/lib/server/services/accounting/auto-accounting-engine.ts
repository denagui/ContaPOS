export interface JournalEntryData {
  organizationId: string;
  branchId?: string;
  documentType: 'sale' | 'purchase' | 'expense' | 'payment' | 'receipt' | 'adjustment';
  documentId: string;
  description: string;
  reference?: string;
  transactionDate: number;
  lines: JournalLineData[];
  autoGenerated?: boolean;
  aiExplanation?: string;
  createdBy?: string;
}

export interface JournalLineData {
  accountCode: string; // Código de cuenta (ej: "1-01-001")
  description?: string;
  debitCents: number;
  creditCents: number;
  contactId?: string;
  productId?: string;
  branchId?: string;
}

export interface AccountingRule {
  documentType: string;
  lines: Array<{
    accountType: string;
    accountSubtype?: string;
    direction: 'debit' | 'credit';
    calculation: 'subtotal' | 'tax' | 'total' | 'fixed';
    fixedAmountCents?: number;
  }>;
}

